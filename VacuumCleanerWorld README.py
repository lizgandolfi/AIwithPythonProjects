#README for VacuumCleanerWorld:

##This code simulates a vacuum cleaner environment in Python. It includes an environment model and a simple reflex agent to clean the environment. The agent can be tested with various initial dirt configurations and agent locations.

#To run the code:

##Make sure to "import random" at the beginning. 
##Pseudocode for Exercise 4 starts in line 5 until 82, it is commented out. The implementable code for Exercise 5 starts in line 90 and goes to line 200. 
##It returns dirt configurations and the score associated to each trial. If the vacuum's location is dirty and it successfully cleans it, a value of 10 is given.
##It also returns an average performance score for the trials completed with each run.
